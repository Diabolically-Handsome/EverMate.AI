def tr(lang: str, key: str, **kwargs) -> str:
    d = TEXTS.get(lang, TEXTS["zh"])
    text = d.get(key, key)
    if kwargs:
        try:
            return text.format(**kwargs)
        except (KeyError, IndexError):
            return text
    return text
